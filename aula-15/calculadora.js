function soma(n1, n2) {
    return n1 + n2;
}

function subt(n1, n2) {
    return n1 - n2;
}

function multiplicar(n1, n2) {
    return n1 * n2;
}

function dividir(n1, n2) {
    return n1 / n2;
}

export { soma, subt, multiplicar, dividir }; //exportar
export default soma ; //importou calculadora ja importa o soma